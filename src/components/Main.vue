        }
      },
      async addGuestbook(){ 
        const params = {
          name: this.guestName ? this.guestName : 'anonymous',
          message: this.guestMessage
        }
        if(!this.guestName || !this.guestMessage) return;
        try{
          await axios({
              url: '/guestbook',
              method: 'post',
              data: params
          });  
          this.resetMessage();
          this.getGuestbook();
          this.nameDisabled = true;
        } catch(err){ 
          console.log(err)
        }
      },

      resetMessage(){
        this.guestMessage = '';
      },

      parseDate(date){
        let parse = new Date(date);
        const format = 'DD-MM-YYYY HH:mm:ss';
        const formatDate = moment(parse).format(format); 
        return formatDate;

      }
    },
    mounted() { 
      let urlParams = new URLSearchParams(window.location.search);
      this.guestName = urlParams.get('tamu');  
      this.getGuestbook();
    },
